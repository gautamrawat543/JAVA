package Revision;

public class Comparision_Operators {
    // Comparison operators are used to compare two values and return a boolean
    // result. Here are some common comparison operators:

    public static void main(String[] args) {
        int x = 5;
        int y = 10;

        // Equal to (==): Returns true if two values are equal.
        System.out.println(x == y); // Outputs: false

        // Not equal to (!=): Returns true if two values are not equal.
        System.out.println(x != y); // Outputs: true

        // Greater than (>): Returns true if the left operand is greater than the right
        // operand.
        System.out.println(x > y); // Outputs: false

        // Less than (<): Returns true if the right operand is greater than the left
        // operand.
        System.out.println(x < y); // Outputs: true

        // Greater than or equal to (>=): Returns true if the left operand is greater
        // than or equal to the right operand.
        System.out.println(x >= y); // Outputs: false

        // Less than or equal to (<=): Returns true if the left operand is less than or
        // equal to the right operand.
        System.out.println(x <= y); // Outputs: true
    }
}
